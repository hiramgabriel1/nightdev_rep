export function t(lang: string | null | undefined, key: string, ...args: (string | number)[]): string {
  const l = lang === 'es' ? 'es' : 'en'
  let msg = messages[l]?.[key] || messages.en[key] || key
  args.forEach((arg, i) => {
    msg = msg.replace(`{{${i}}}`, String(arg))
  })
  return msg
}

export function getLangFromDb(dbUser: { language?: string | null } | null): string {
  return dbUser?.language || 'en'
}
